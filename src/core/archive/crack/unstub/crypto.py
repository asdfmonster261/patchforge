def steam_xor(data: bytearray, size: int, key: int = 0) -> int:
    """
    XOR-decrypt *data* in-place using a rolling 32-bit key.

    If *key* is 0 the first 4 bytes of *data* are consumed as the initial key
    and decoding starts from offset 4.

    Returns the final key value (used to chain into payload decoding).
    """
    if size < 4:
        return key

    offset = 0
    if key == 0:
        offset = 4
        key = struct.unpack_from("<I", data, 0)[0]

    pos = offset
    while pos + 4 <= size:
        val = struct.unpack_from("<I", data, pos)[0]
        struct.pack_into("<I", data, pos, (val ^ key) & 0xFFFFFFFF)
        key = val
        pos += 4

    return key


def xtea_decrypt(keys: list, v1: int, v2: int, rounds: int = 32) -> Tuple[int, int]:
    """Decrypt one 64-bit XTEA block (two uint32 values)."""
    DELTA = 0x9E3779B9
    MASK = 0xFFFFFFFF
    total = (DELTA * rounds) & MASK

    for _ in range(rounds):
        v2 = (v2 - (((v1 << 4 ^ v1 >> 5) + v1) ^ (total + keys[(total >> 11) & 3]))) & MASK
        total = (total - DELTA) & MASK
        v1 = (v1 - (((v2 << 4 ^ v2 >> 5) + v2) ^ (total + keys[total & 3]))) & MASK

    return v1, v2


def steam_drmp_decrypt(data: bytearray, size: int, keys: list) -> None:
    """
    Decrypt the SteamDRMP.dll blob in-place.

    Uses XTEA in a CBC-like mode with an XOR feedback chain.
    """
    v1_prev = 0x55555555
    v2_prev = 0x55555555

    for x in range(0, size & ~7, 8):
        d1 = struct.unpack_from("<I", data, x)[0]
        d2 = struct.unpack_from("<I", data, x + 4)[0]

        r1, r2 = xtea_decrypt(keys, d1, d2)

        struct.pack_into("<I", data, x,     (r1 ^ v1_prev) & 0xFFFFFFFF)
        struct.pack_into("<I", data, x + 4, (r2 ^ v2_prev) & 0xFFFFFFFF)

        v1_prev = d1
        v2_prev = d2


