def aes_rebuild_iv(key: bytes, iv: bytes) -> bytes:
    """
    Rebuild the AES IV by ECB-decrypting it with the key.
    """
    return _AES(key).decrypt_block(iv)


def aes_decrypt_cbc(data: bytes, key: bytes, iv: bytes) -> bytes:
    """AES-CBC decryption with no padding.  Only full 16-byte blocks are
    decrypted; any trailing bytes shorter than a block are kept as-is."""
    aes = _AES(key)
    block_size = 16
    full_len = (len(data) // block_size) * block_size
    out = bytearray()
    prev_block = bytearray(iv)

    for i in range(0, full_len, block_size):
        block = data[i:i + block_size]
        decrypted = aes.decrypt_block(block)
        out.extend(a ^ b for a, b in zip(decrypted, prev_block))
        prev_block = bytearray(block)

    # Append any remaining bytes that don't form a full block (unencrypted tail)
    if full_len < len(data):
        out.extend(data[full_len:])

    return bytes(out)


